package com.taole.member.domain.bo;

import java.util.Date;

public class SalesStatResult {

	/**
	 * 店面ID
	 */
	private String shopId;
	/**
	 * 店面名称
	 */
	private String shopName;
	
	/**
	 * 统计周期（日期yyyy-MM-dd/月yyyy-MM）
	 */
	private Date statDate;
	
	/**
	 * 统计对象Id
	 */
	private String objectId;
	
	/**
	 * 统计对象名称
	 */
	private String objectName;
	
	/**
	 * 统计对象单位
	 */
	private String objectUnit;
	
	/**
	 * 统计金额
	 */
	private Double statMoney;
	
	/**
	 * 统计数量
	 */
	private Integer statAmount;

	public String getShopId() {
		return shopId;
	}

	public void setShopId(String shopId) {
		this.shopId = shopId;
	}

	public String getShopName() {
		return shopName;
	}

	public void setShopName(String shopName) {
		this.shopName = shopName;
	}

	public Date getStatDate() {
		return statDate;
	}

	public void setStatDate(Date statDate) {
		this.statDate = statDate;
	}

	public String getObjectId() {
		return objectId;
	}

	public void setObjectId(String objectId) {
		this.objectId = objectId;
	}

	public String getObjectName() {
		return objectName;
	}

	public void setObjectName(String objectName) {
		this.objectName = objectName;
	}

	public Double getStatMoney() {
		return statMoney;
	}

	public void setStatMoney(Double statMoney) {
		this.statMoney = statMoney;
	}

	public Integer getStatAmount() {
		return statAmount;
	}

	public void setStatAmount(Integer statAmount) {
		this.statAmount = statAmount;
	}

	public String getObjectUnit() {
		return objectUnit;
	}

	public void setObjectUnit(String objectUnit) {
		this.objectUnit = objectUnit;
	}
	
	
}
